package Mentoring_Java_Advance;

public class For_Loop {


    public static void main(String[] args) {
        for (int i=0;i<10;i+=2){
            System.out.println(i);

        }

        int age =10;
        while ( age<20){
            System.out.println("he is 10 years old");
            age ++;
        }
        int num=10;
        do {
            System.out.println("we are learning");
            num++;
        }while (num<20);

    }

}
